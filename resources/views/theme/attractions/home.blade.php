                                                            altInput: true,
                                                            altFormat: "j M Y",
                                                            dateFormat: "Y-m-d",
                                                            defaultDate: result,
                                                            minDate: p,
                                                            disableMobile: "true"

                                                        });
                                                    } else {
                                                        flatpickr("#check_out", {
                                                            altInput: true,
                                                            altFormat: "j M Y",
