                                                    var p = $(this).val();
                                                    var r = $("#check_out").val();

                                                    var pickup_d = new Date(p);
                                                    var return_d = new Date(r);

                                                    if (pickup_d >= return_d) {
                                                        pickup_d.setDate(pickup_d.getDate() + 1);

                                                        var result = pickup_d.getFullYear() + '-' + (pickup_d.getMonth() + 1) + '-' + pickup_d.getDate();

